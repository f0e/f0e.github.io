<h3>options</h3>
<div>recording options can be edited in hlae_settings.cfg</div>

<br />

<div class="list">
	<h3>commands</h3>
	<ul>
		<li>rec - start recording</li>
		<li>end - end recording</li>
		<li>toggle_players - toggle hiding players</li>
	</ul>
</div>

<div class="list">
	<h3>configs</h3>
	<div class="muted">note: all of these configs require hlae_base.cfg and hlae_settings.cfg.</div>
	<ul>
		<li>hlae.cfg</li>
		<span class="muted">regular recording</span>

		<li>hlae_blur.cfg</li>
		<span class="muted">regular recording with motion blur</span>

		<li>hlae_vm.cfg</li>
		<span class="muted"
			>regular recording - records game and viewmodel streams into seperate files</span
		>

		<li>hlae_adv.cfg</li>
		<span class="muted"
			>advanced recording - records game, depth, viewmodel and hud streams into seperate video files</span
		>
	</ul>
</div>
